#include "libft.h"

int	*ft_bubble(int *arr, int len)
{
	int i;
	int j;

	i = 1;
	while (i < len)
	{
		j = 0;
		while (j < len - i)
		{
			if (arr[j] > arr[j + 1])
				ft_swap(&arr[j], &arr[j + 1]);
			j++;
		}
		i++;
	}
	return (arr);
}
